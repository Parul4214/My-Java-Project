//three difference method in one class

public class SimpleMethod {

	int num1 = 5;
	int num2 = 15;

	// add Two number method created with void return type
	void addTwoNumber() {

		int sum = num1 + num2;
		System.out.println("Addition of two number " + sum);
	}

	// multiply two number method created with void return type
	void multiplyTwonumber() {

		int num_multiple = num1 * num2;
		System.out.println("Multiplication of two numers " + num_multiple);
	}

	// subtract two number method created with void return type
	void subtractTwonumbers() {
		int subs = num2 - num1;
		System.out.println("Substracion of two numebers " + subs);

	}
}
